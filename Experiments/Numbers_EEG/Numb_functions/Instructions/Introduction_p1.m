In the following experiment, you will see a stream of 10 numbers in two
different colours (orange and blue).

Your task is to decide at the end of the trial which colour had the 
highest mean. 
At the end of the trial you will see two boxes, containing
the letter B (blue) or O (orange). Use the 'F' key to choose the left option
and the 'J' key to choose the right option. If B is the left option, it will
remain in that position for the whole experiment.

If you don't respond within 2 seconds, the trial will end and the response 
will be recorded as being incorrect.

You will start with 10 practice trials.