Well done! The farmer is back and now you can start helping him out.

On every turn, you will be presented with 2 donkeys, one at a time.
At the end you have to decide which donkey you want. After choosing a donkey
you will see whether the donkey actually delivered 13 golden coins or not,
with the same feedback signs as in the previous part ('$' and high tone for
reward and '/' and low tone for no reward).

Because you're helping out on the farm, on half of the trials 
the farmer will tell you which donkey you have to choose, indicated by
a coloured frame around the donkey.
=> This does not mean he will show you the donkey most likely to deliver!!

If you fail to choose one of the two within 2 seconds, you lose their 
interest and none of the two returns any coins. 