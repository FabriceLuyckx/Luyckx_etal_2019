Imagine you just landed in a fairy tale world where all donkeys
deliver golden coins. In order to get back home, you need to collect as 
many coins as possible. A nice farmer decides to help you and offers 
you his 6 donkeys for a short while.

But before you can use the donkeys yourself, the farmer wants
you to work on his farm and help him out. This is an opportunity for
you to figure out which donkeys are more profitable than others.

ALL of them will deliver coins, but each donkey has its own idea of
how *often* it will deliver you coins. These probabilities will remain the 
same for each donkey throughout the whole experiment and never change!