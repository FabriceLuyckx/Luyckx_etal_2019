Before you start work on the farm, the farmer wants you to get to know
the different donkeys. All six are standing in a field nearby.

Each time you pat (click) the donkey, you will receive feedback on whether 
you received golden coins or not.

        COINS       = '$' + high tone
        NO COINS    = '/' + low tone
        
You have to click on each donkey 36 times.

The coins will NOT count for your monetary bonus at the end of the experiment.